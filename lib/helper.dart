final idType = "INTEGER PRIMARY KEY AUTOINCREMENT";
final textType = "TEXT";
final emailType = "TEXT NOT NULL UNIQUE";

bool checkEmail(String email) {
  bool emailValid = RegExp(
          r"^[a-zA-Z0-9.a-zA-Z0-9.!#$%&'*+-/=?^_`{|}~]+@[a-zA-Z0-9]+\.[a-zA-Z]+")
      .hasMatch(email);
  return emailValid;
}
